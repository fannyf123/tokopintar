<div class="tab-pane fade" id="tab-laporan">
    <h4 class="fw-bold mb-3"><i class="fas fa-chart-line text-primary me-2"></i>Lihat Laporan Untung</h4>
    <p class="text-muted">Untuk tahu untung-rugi toko per hari, bulan, atau tahun.</p>

    <h6 class="fw-bold mt-4 mb-2">Cara Lihat Laporan</h6>
    <div class="panduan-step">
        <span class="step-num">1</span> Buka <strong>Pengaturan → Laporan Untung</strong>
    </div>
    <div class="panduan-step">
        <span class="step-num">2</span> Pilih cepat: <strong>Hari Ini / Kemarin / 7 Hari / 30 Hari / Bulan Ini / Tahun Ini</strong>
    </div>
    <div class="panduan-step">
        <span class="step-num">3</span> Atau pilih manual tanggal mulai &amp; tanggal akhir → klik Terapkan
    </div>
    <div class="panduan-step">
        <span class="step-num">4</span> Pilih cara tampilan: Harian / Mingguan / Bulanan / Tahunan
    </div>

    <h6 class="fw-bold mt-4 mb-2">Yang Bisa Dilihat</h6>
    <ul>
        <li><strong>Penjualan (Omzet)</strong> - total uang masuk dari jualan</li>
        <li><strong>HPP (Harga Pokok Penjualan)</strong> - total modal barang yang sudah terjual</li>
        <li><strong>Laba Kotor</strong> = Omzet - HPP (untung kotor)</li>
        <li><strong>Biaya</strong> - biaya operasional (sewa, listrik, gaji) di periode itu</li>
        <li><strong>Laba Bersih</strong> = Laba Kotor - Biaya (untung bersih, ini yang masuk kantong)</li>
    </ul>

    <h6 class="fw-bold mt-4 mb-2">Ekspor Laporan</h6>
    <div class="panduan-step">
        <span class="step-num">📄</span><strong>PDF</strong> - untuk dicetak / dilampirkan ke laporan pajak
    </div>
    <div class="panduan-step">
        <span class="step-num">📊</span><strong>CSV</strong> - buka di Excel kalau mau analisis tambahan
    </div>

    <h6 class="fw-bold mt-4 mb-2">Catat Biaya Operasional</h6>
    <p class="small text-muted">Supaya angka Laba Bersih akurat, jangan lupa catat biaya rutin:</p>
    <div class="panduan-step">
        Buka <strong>Pengaturan → Biaya Operasional → + Catat Biaya Baru</strong>. Pilih jenis (Sewa / Listrik / Gaji / Lainnya), tanggal, jumlah. Sertakan foto bukti (struk PLN, dll) kalau ada.
    </div>

    <div class="panduan-tip">
        💡 <strong>Tips:</strong> Cek laporan TIAP MINGGU. Kalau Laba Bersih turun terus, periksa: harga jual kekecilan? Biaya sewa naik? Banyak diskon? Laporan ini detektor masalah.
    </div>
</div>
