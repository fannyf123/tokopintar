    <h4 class="fw-bold mb-3"><i class="fas fa-play-circle text-primary me-2"></i>Memulai Pakai TOKOPINTAR</h4>
    <p class="text-muted">Selamat datang! Berikut langkah awal untuk siapkan aplikasi sesuai toko Anda.</p>

    <div class="panduan-step">
        <span class="step-num">1</span><strong>Login dengan akun Anda</strong>
        <p class="mb-0 mt-2 small text-muted">Akun bawaan untuk demo:<br>
        • Pemilik toko: <code>admin</code> / <code>admin123</code><br>
        • Kasir: <code>kasir</code> / <code>kasir123</code><br>
        • Bagian gudang: <code>gudang</code> / <code>gudang123</code></p>
    </div>

    <div class="panduan-step">
        <span class="step-num">2</span><strong>Ubah password lewat menu Profil</strong>
        <p class="mb-0 mt-2 small text-muted">Klik nama Anda di topbar atau menu Profil di sidebar &gt; Ganti Password. Demi keamanan, JANGAN tetap pakai password bawaan.</p>
    </div>

    <div class="panduan-step">
        <span class="step-num">3</span><strong>Sesuaikan tampilan</strong>
        <p class="mb-0 mt-2 small text-muted">
        • Klik tombol panah di pojok atas sidebar untuk minimize/expand menu (saat butuh layar lebih lebar)<br>
        • Toggle <strong>Mode Gelap/Terang</strong> di sidebar bawah, sesuai selera
        </p>
    </div>

    <div class="panduan-step">
        <span class="step-num">4</span><strong>Sesuaikan barang dengan toko Anda</strong>
        <p class="mb-0 mt-2 small text-muted">Aplikasi sudah berisi 135 barang umum (Indomie, Aqua, dll). Anda bisa: edit harga, hapus yang tidak dijual, atau tambah barang baru. Lihat tab "Atur Barang".</p>
    </div>

    <div class="panduan-step">
        <span class="step-num">5</span><strong>Coba transaksi pertama</strong>
        <p class="mb-0 mt-2 small text-muted">Buka menu <strong>Kasir</strong> &gt; cari/scan barang &gt; tambah ke keranjang &gt; masukkan uang &gt; klik Selesaikan Bayar. Selesai, struk siap dicetak.</p>
    </div>

    <div class="panduan-tip">
        💡 <strong>Tips:</strong> Pakai HP buat scan barcode dengan tap tombol kamera di Kasir. Praktis, tidak perlu ketik kode panjang.
    </div>
</div>
