    <div class="panduan-step">
        <span class="step-num">1</span><strong>Login dengan akun Anda</strong>
        <p class="mb-0 mt-2 small text-muted">Akun bawaan untuk demo:<br>
        • Pemilik toko: <code>admin</code> / <code>admin123</code><br>
        • Kasir: <code>kasir</code> / <code>kasir123</code><br>
        • Bagian gudang: <code>gudang</code> / <code>gudang123</code></p>
    </div>

    <div class="panduan-step">
        <span class="step-num">2</span><strong>Ubah password lewat menu Profil</strong>
        <p class="mb-0 mt-2 small text-muted">Klik nama Anda di pojok kanan atas → Profil → Ganti Password. Demi keamanan, JANGAN tetap pakai password bawaan.</p>
    </div>

    <div class="panduan-step">
        <span class="step-num">3</span><strong>Sesuaikan barang dengan toko Anda</strong>
        <p class="mb-0 mt-2 small text-muted">Aplikasi sudah berisi 135 barang umum (Indomie, Aqua, dll). Anda bisa: edit harga, hapus yang tidak dijual, atau tambah barang baru. Lihat tab "Atur Barang".</p>
    </div>

    <div class="panduan-step">
        <span class="step-num">4</span><strong>Coba transaksi pertama</strong>
        <p class="mb-0 mt-2 small text-muted">Buka menu <strong>Kasir</strong> → cari barang → tambah ke keranjang → masukkan uang → klik Bayar. Selesai, struk siap dicetak.</p>
    </div>

    <div class="panduan-tip">
        💡 <strong>Tips:</strong> Kalau mau pegang HP buat scan barcode, cukup tap tombol kamera di Kasir. Kamera HP akan langsung baca barcode produk.
