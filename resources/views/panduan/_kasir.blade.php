<div class="tab-pane fade" id="tab-kasir">
    <h4 class="fw-bold mb-3"><i class="fas fa-cash-register text-primary me-2"></i>Cara Jualan / Pakai Kasir</h4>
    <p class="text-muted">Halaman Kasir adalah tempat utama untuk transaksi dengan pelanggan.</p>

    <h6 class="fw-bold mt-4 mb-2">A. Tambah Barang ke Keranjang</h6>
    <div class="panduan-step">
        <span class="step-num">1</span><strong>Cara 1 - Cari Manual:</strong> Ketik nama barang di kotak pencarian (contoh: "indomie") &gt; klik dari hasil yang muncul &gt; masuk keranjang.
    </div>
    <div class="panduan-step">
        <span class="step-num">2</span><strong>Cara 2 - Scan Barcode pakai HP:</strong> Tap tombol hijau <strong>📷 Scan</strong> &gt; izinkan kamera (sekali saja) &gt; arahkan ke barcode pada kemasan &gt; barang otomatis masuk + kamera tutup sendiri.
        <p class="mb-0 mt-2 small text-muted"><strong>Tap kedua = tutup kamera.</strong> Kalau lampu indikator kamera masih menyala setelah tutup, refresh halaman.</p>
    </div>
    <div class="panduan-step">
        <span class="step-num">3</span><strong>Ubah Jumlah:</strong> Di kolom Qty, ubah angka untuk beli lebih dari 1.
    </div>
    <div class="panduan-step">
        <span class="step-num">4</span><strong>Hapus Barang:</strong> Klik tombol ✕ merah di baris itu kalau salah ambil.
    </div>

    <h6 class="fw-bold mt-4 mb-2">B. Pilih Pelanggan</h6>
    <div class="panduan-step">
        <span class="step-num">1</span><strong>Klik kotak Pelanggan</strong> &gt; ketik nama atau no HP (langsung filter saat ngetik). Cocok untuk database pelanggan banyak.
    </div>
    <div class="panduan-step">
        <span class="step-num">2</span><strong>Pelanggan Member</strong> ditandai badge biru <span class="badge bg-primary" style="font-size:9px">MEMBER</span>. Saat dipilih, diskon otomatis terisi sesuai persen yang sudah di-set di profil pelanggan.
    </div>
    <div class="panduan-step">
        <span class="step-num">3</span><strong>Kosongkan</strong> kolom pelanggan kalau pembeli umum (tidak terdaftar).
    </div>

    <h6 class="fw-bold mt-4 mb-2">C. Pembayaran</h6>
    <div class="panduan-step">
        <span class="step-num">1</span><strong>Cara Bayar:</strong> Tunai / Transfer Bank / QRIS / Kartu Debit/Kredit.
    </div>
    <div class="panduan-step">
        <span class="step-num">2</span><strong>Potongan & Pajak:</strong> Bisa toggle antara <strong>Rp</strong> (nominal pas) atau <strong>%</strong> (persenan dari subtotal). Klik tombol kecil di label untuk switch mode.
    </div>
    <div class="panduan-step">
        <span class="step-num">3</span><strong>Uang Diterima:</strong> Ketik nominal yang dibayar pelanggan. Otomatis format ribuan (10000 jadi 10.000). Kembalian muncul real-time.
    </div>
    <div class="panduan-step">
        <span class="step-num">4</span> Klik <strong>Selesaikan Bayar</strong> hijau besar &gt; otomatis pindah ke halaman struk &gt; klik <strong>Cetak Struk</strong>.
    </div>

    <h6 class="fw-bold mt-4 mb-2">D. Shortcut Keyboard (Bonus)</h6>
    <ul class="small">
        <li><kbd>F2</kbd> atau <kbd>Ctrl+K</kbd> atau <kbd>/</kbd> - fokus ke kotak pencarian</li>
        <li><kbd>F4</kbd> atau <kbd>Ctrl+Enter</kbd> - klik tombol Selesaikan Bayar</li>
        <li><kbd>Esc</kbd> - clear search atau kosongkan keranjang</li>
    </ul>

    <div class="panduan-tip">
        💡 <strong>Tips kasir cepat:</strong> Pakai HP berdiri sebagai scanner. Tap 📷 sekali, scan semua barang berturut-turut, baru ke pembayaran.
    </div>
</div>
