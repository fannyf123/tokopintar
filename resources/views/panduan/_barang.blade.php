<div class="tab-pane fade" id="tab-barang">
    <h4 class="fw-bold mb-3"><i class="fas fa-box text-primary me-2"></i>Atur Barang Toko</h4>
    <p class="text-muted">Sebelum jualan, pastikan daftar barang sesuai stok toko Anda.</p>

    <h6 class="fw-bold mt-4 mb-2">A. Tambah Barang Baru</h6>
    <div class="panduan-step">
        <span class="step-num">1</span> Klik menu <strong>Stok Barang &gt; Daftar Barang</strong>
    </div>
    <div class="panduan-step">
        <span class="step-num">2</span> Klik tombol biru <strong>+ Tambah Barang Baru</strong>
    </div>
    <div class="panduan-step">
        <span class="step-num">3</span> Isi form:
        <ul class="mt-2 mb-0 small">
            <li><strong>Kode Barang</strong> - kosongkan saja, otomatis dibuat sistem (BRG00001 dst)</li>
            <li><strong>Barcode</strong> - 3 cara isi:
                <ul>
                    <li>Tap 📷 untuk <strong>scan kemasan</strong> pakai kamera HP (paling akurat)</li>
                    <li>Tap 🔍 untuk <strong>cari online</strong> dari nama produk (untuk produk yang ada di OpenFoodFacts)</li>
                    <li>Atau ketik manual</li>
                </ul>
            </li>
            <li><strong>Nama</strong> - contoh "Indomie Goreng 85g"</li>
            <li><strong>Kategori</strong> - pilih (Mie Instan / Air Mineral / dll)</li>
            <li><strong>Pemasok</strong> - dari mana barang biasa dibeli</li>
            <li><strong>Satuan</strong> - pcs, kg, liter, botol, dll</li>
            <li><strong>Modal Beli</strong> - harga modal per satuan (Rp)</li>
            <li><strong>Harga Jual</strong> - harga ke pelanggan (Rp)</li>
            <li><strong>Stok Minimal</strong> - kalau stok &le; angka ini, akan keluar notifikasi "Stok Menipis" di Dashboard</li>
            <li><strong>Status Barang</strong> - "Dijual" (muncul di kasir) atau "Disembunyikan" (data lama tetap aman tapi tidak muncul)</li>
        </ul>
    </div>
    <div class="panduan-step">
        <span class="step-num">4</span> Klik <strong>Simpan</strong>. Barang siap dijual.
    </div>

    <h6 class="fw-bold mt-4 mb-2">B. Edit / Ubah Harga</h6>
    <div class="panduan-step">Klik tombol biru ✏️ di baris barang &gt; ubah harga / data &gt; Simpan.</div>

    <h6 class="fw-bold mt-4 mb-2">C. Sembunyikan Barang</h6>
    <div class="panduan-step">Edit barang &gt; ubah <strong>Status Barang</strong> ke "Disembunyikan" &gt; Simpan. Barang tidak akan muncul di Kasir tapi data lama tetap aman.</div>

    <h6 class="fw-bold mt-4 mb-2">D. Hapus Permanen</h6>
    <div class="panduan-step">Klik 🗑️ merah di baris barang. Barang yang sudah punya histori transaksi tidak bisa dihapus, hanya bisa di-Sembunyikan demi audit trail.</div>

    <div class="panduan-tip">
        💡 Untuk produk Indonesia populer (Aqua, Indomie, Royco), cari online sering kosong di OpenFoodFacts. Yang paling akurat: tap 📷 dan scan kemasan asli.
    </div>
</div>
