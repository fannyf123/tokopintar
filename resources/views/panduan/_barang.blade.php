<div class="tab-pane fade" id="tab-barang">
    <h4 class="fw-bold mb-3"><i class="fas fa-box text-primary me-2"></i>Atur Barang Toko</h4>
    <p class="text-muted">Sebelum jualan, pastikan daftar barang sesuai stok toko Anda.</p>

    <h6 class="fw-bold mt-4 mb-2">A. Tambah Barang Baru</h6>
    <div class="panduan-step">
        <span class="step-num">1</span> Klik menu <strong>Stok Barang → Daftar Barang</strong>
    </div>
    <div class="panduan-step">
        <span class="step-num">2</span> Klik tombol biru <strong>+ Tambah Barang Baru</strong>
    </div>
    <div class="panduan-step">
        <span class="step-num">3</span> Isi form:
        <ul class="mt-2 mb-0 small">
            <li><strong>Kode Barang</strong> - kosongkan saja, otomatis dibuat sistem</li>
            <li><strong>Barcode</strong> - scan kemasan pakai tombol 📷, atau cari online dengan tombol 🔍, atau ketik manual</li>
            <li><strong>Nama</strong> - contoh "Indomie Goreng 85g"</li>
            <li><strong>Kategori</strong> - pilih (Mie Instan / Air Mineral / dll)</li>
            <li><strong>Pemasok</strong> - pilih dari mana barang ini biasa dibeli</li>
            <li><strong>Satuan</strong> - pcs, kg, liter, botol, dll</li>
            <li><strong>Modal Beli</strong> - harga modal per satuan (Rp)</li>
            <li><strong>Harga Jual</strong> - harga ke pelanggan (Rp)</li>
            <li><strong>Stok Minimal</strong> - kalau stok ≤ angka ini, akan keluar notifikasi "Stok Menipis" di Dashboard</li>
        </ul>
    </div>
    <div class="panduan-step">
        <span class="step-num">4</span> Klik <strong>Simpan</strong>. Barang siap dijual.
    </div>

    <h6 class="fw-bold mt-4 mb-2">B. Edit / Ubah Harga</h6>
    <div class="panduan-step">Klik tombol biru ✏️ di baris barang → ubah harga / data → Simpan.</div>

    <h6 class="fw-bold mt-4 mb-2">C. Sembunyikan Barang yang Tidak Dijual Lagi</h6>
    <div class="panduan-step">Edit barang → ubah <strong>Status Barang</strong> ke "Disembunyikan" → Simpan. Barang tidak akan muncul di Kasir tapi data lama tetap aman.</div>

    <div class="panduan-tip">
        💡 Untuk produk Indonesia populer seperti Aqua/Indomie, barcode-nya bisa Anda scan dari kemasan asli pakai HP. Lebih akurat daripada ketik manual.
    </div>
</div>
